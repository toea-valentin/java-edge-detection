package pack2;

public class ImageUser { //class for holding the metadata of an image: height, width 
						//with getters and setters
	private int height, width;

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
}
